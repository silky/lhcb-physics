      INTEGER*4 FUNCTION UPCOM_ADDRESS()
      IMPLICIT NONE

      DOUBLE COMPLEX COLMAT,BUNDAMP
      DOUBLE PRECISION ECM,PMBC,PMB,PMC,FBCC,PMOMUP,
     &     PMOMZERO 
      COMMON/UPCOM/ECM,PMBC,PMB,PMC,FBCC,PMOMUP(5,8),
     & 	COLMAT(10,64),BUNDAMP(4),PMOMZERO(5,8)
 
      INTEGER*4 GETADDR
 
      UPCOM_ADDRESS = GETADDR(ECM)
      RETURN
      END
