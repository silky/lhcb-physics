      VALUE = LHAVALUE( INDEX ) 
      END


      SUBROUTINE GSETLHAVALUE( INDEX , VALUE ) 
      IMPLICIT NONE 
      
      INTEGER INDEX
      DOUBLE PRECISION VALUE

      CHARACTER*20 LHAPARM(20)
      DOUBLE PRECISION LHAVALUE(20)
      COMMON/LHACONTROL/LHAPARM,LHAVALUE

      LHAVALUE( INDEX ) = VALUE
      END

      SUBROUTINE LHAPDFGAUSS_INIT( INDEX , FILENAME ) 
*   if INDEX = 0 , SUPPRESS Output. 
*   if INDEX = 1 , KEEP Output
      IMPLICIT NONE
      INTEGER INDEX
      CHARACTER*(*) FILENAME
*
      COMMON /GAUSSLHAPDF/ IPDFUNIT
      INTEGER IPDFUNIT
*
      EXTERNAL LHAPDFSET
*
      IF ( INDEX .EQ. 0 ) THEN
        IPDFUNIT = 74
        OPEN( UNIT = 74 , FILE = FILENAME( 1:LEN(FILENAME) ) ) 
      ELSE
        IPDFUNIT = 6 
      ENDIF
*
      END
*
*
      SUBROUTINE LHAPDFGAUSS_END
      IMPLICIT NONE
      CLOSE( UNIT = 74 ) 
      END

