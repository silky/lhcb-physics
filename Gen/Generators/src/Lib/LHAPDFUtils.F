      SUBROUTINE GETLHAPARM( INDEX , VALUE ) 
      IMPLICIT NONE 
      
      INTEGER INDEX
      CHARACTER*20 VALUE

      CHARACTER*20 LHAPARM(20)
      DOUBLE PRECISION LHAVALUE(20)
      COMMON/LHACONTROL/LHAPARM,LHAVALUE

      VALUE = LHAPARM( INDEX ) 
      END


      SUBROUTINE SETLHAPARM( INDEX , VALUE ) 
      IMPLICIT NONE 
      
      INTEGER INDEX
      CHARACTER*(*) VALUE

      CHARACTER*20 LHAPARM(20)
      DOUBLE PRECISION LHAVALUE(20)
      COMMON/LHACONTROL/LHAPARM,LHAVALUE

      LHAPARM( INDEX ) = VALUE(1:LEN(VALUE))
      END

